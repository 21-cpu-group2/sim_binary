    vd result = {0, 32};

    vd ram_read_top = {0, 23};
    vd ram_read_bottom = {0, 13};
    vd ram_main = {0, 24};
    vd ram_grad = {0, 13};

    vd exp = concat2(constant(0, 1), slice(op, 30, 23));
    vd exp_plus126 = add(exp, constant(126, 9));
    vd exp_plus127 = add(exp, constant(127, 9));
    vd for_exp2 = (slice(op, 23, 23).data) ? exp_plus127 : exp_plus126 ;

    vd exp2 = {0, 9};
    vd res = {0, 14};
    vd exp3 = {0, 8};
    vd grad_mul_res = {0, 27};
    vd frac = {0, 24};

    vd grad_mul_res_need = {0, 24};
    vd result_1 = {0, 24};
    vd result_plus1_1 = {0, 24};


    // first clk;
    assign(&result, constant(0, 32), -1, -1);
    assign(&exp2, constant(0, 9), -1, -1);
    assign(&exp3, constant(0, 8), -1, -1);
    assign(&grad_mul_res, constant(0, 27), -1, -1);
    assign(&frac, constant(0, 24), -1, -1);

    // second clk;
    assign(&ram_read_top, constant(ram[slice(op, 23, 14).data].fra, 23), -1, -1);
    assign(&ram_read_bottom, constant(ram[slice(op, 23, 14).data].gra, 13), -1, -1);
