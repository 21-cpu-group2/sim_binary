    if (slice(grad_mul_res, 3, 3).data) {
        assign(&result, concat3(constant(0,1), exp3, slice(result_plus1_1, 22, 0)), -1, -1);
    }
    else {
        assign(&result, concat3(constant(0,1), exp3, slice(result_1, 22, 0)), -1, -1);
    }
    return result;
